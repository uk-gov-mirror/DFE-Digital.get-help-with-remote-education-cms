Remote education resources, including lessons from Oak National Academy, as well as guidance, lesson plans and webinars on teaching remotely.

Schools
=======

Guidance
--------

A sector-led [remote education good practice guide](https://www.gov.uk/government/publications/remote-education-good-practice") to support school leaders in developing remote education.

The [Education Endowment Foundation Covid-19 support guide for schools](https://educationendowmentfoundation.org.uk/covid-19-resources/national-tutoring-programme/covid-19-support-guide-for-schools/#closeSignup) includes information on how to support effective remote education.

Resources and curriculum support
--------------------------------

[Example lesson plans](https://www.gov.uk/government/publications/example-lessons-for-remote-teaching") shared by schools to help teachers adapt their classroom practice for remote education.

### All key stages

Video and interactive lessons from [Oak National Academy](https://www.thenational.academy/) for reception up to year 11 to support remote education.

Primary and secondary resources from [BBC Bitesize](https://www.bbc.co.uk/bitesize) for teachers and pupils.

#### Computing

[Teach Computing](https://teachcomputing.org/?gclid=CjwKCAjw1K75BRAEEiwAd41h1IZ2Rt-61VjxGJ0XDKnw1ft7lMNy5ZjCHYEfSCyJOEokALj28Fc09RoC8nAQAvD_BwE) helps you discover training, resources and guidance to support you in teaching computing with confidence. Computing hubs offer local, tailored support to schools and colleges to improve the teaching of computing and increase participation in computer science.

#### Maths

[Advanced Mathematics Support Programme](https://amsp.org.uk/) aims to increase participation and improve teaching in level 3 maths qualifications.

[The National Centre for Excellence in the Teaching of Mathematics](https://www.ncetm.org.uk/) provides materials and guidance to help schools adapt maths teaching for pupils being educated both remotely and in the classroom.

[Maths Hubs](https://www.ncetm.org.uk/maths-hubs) bring together maths professionals in a collaborative national network of 40 hubs, each led locally by an outstanding school or college, to develop excellent practice.

#### Music

[Music Education Hubs](https://www.artscouncil.org.uk/music-education/music-education-hubs) bring together local authorities, schools and art, community or voluntary organisations to make sure all pupils have access to music education.

#### Relationships, sex, and health education

[Support and training materials](https://www.gov.uk/guidance/teaching-about-relationships-sex-and-health") for schools to help train teachers on relationships, sex, and health education.

### Early Years

Resources from [Hungry Little Minds](https://hungrylittleminds.campaign.gov.uk/), including simple and fun activities for children (from newborn to age five) that can be shared with parents.

### Primary Schools

#### English

[English Hubs](http://www.englishhubs.org/) offer support (resources and training) to eligible, local schools, including local authority maintained schools, academies and free schools. This is to improve teaching of phonics, early language and reading in reception and year 1.

### Secondary schools

#### Citizenship

[The Deliberative Classroom](https://educateagainsthate.com/resources/the-deliberative-classroom/) is a project to support teachers to lead knowledge-based discussions and debates with students on topical issues relating to fundamental British values, citizenship and equality.

#### Modern foreign languages

The [National Centre for Excellence for Language Pedagogy](https://ncelp.org/resources/) (NCELP) works in partnership with university researchers, teacher educators, expert practitioners, and 18 specialist teachers in 9 leading schools across the country. These form language hubs to improve language curriculum design and pedagogy. Support includes professional development tools, teaching resources and workshops.

#### Science

[Isaac Physics](https://isaacphysics.org/) is an online platform that offers support and physics problem solving activities to students transitioning from GCSE to sixth-form college, and progressing to university. It also helps to reduce teacher workload.

[Institute of Physics](https://www.iop.org/education) provides free, bespoke support for teachers of physics to increase the take-up of A level physics, particularly by girls.

[Science Learning Partnerships](https://www.stem.org.uk/science-learning-partnerships) improve science teaching, including facilitating continuous professional development and providing support for schools to increase the take-up of GCSE triple science.

